def is_year(Year):
    if Year < 1752:
        print ("There are no leap year before 1750")
    elif Year % 4==0:
        print ("Leap Year")
    elif Year % 400==0:
        print("Leap year")
    elif Year % 100 !=0:
        print ("No leap year")
#main program
Year = int(input("Please enter the year: "))
is_year(Year)
print()

